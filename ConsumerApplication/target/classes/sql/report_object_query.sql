CREATE TABLE report_object (
                        id int GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                        uuid varchar(100),
                        parent_id varchar(100),
                        stream_id varchar(100),
                        envelope_type varchar(100),
                        processing_date TIMESTAMP,
                        properties text,
                        process_id varchar(100),
                        routeId varchar(100),
                        report_priority varchar(100),
                        report_priority_value int,
                        result int,
                        message varchar(100),
                        report_uuid varchar(100),
                        report_file_name varchar(100),
                        report_file_link varchar(100),
                        report_date varchar(100),
                        report_code varchar(100),
                        report_properties text,
                        report_attachments text,
                        reply_file_name varchar(100),
                        reply_report_file_link varchar(100),
                        document_pack_id varchar(100)
);

CREATE TABLE json_or_string (
                        id int GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                        key_report_object varchar(100),
                        value_report_object varchar(100),
                        report_object_id int REFERENCES report_object(id)
);